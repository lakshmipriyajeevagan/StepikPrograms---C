#include<stdio.h>
int main(){
    int n;
    scanf("%d", &n);
    switch(n){
        case 0: printf("zero\nzero");
            break;
        case 1: printf("one\nunu ");
            break;
        case 2: printf("two\ndoi ");
            break;
        case 3: printf("three\ntrei ");
            break;
        case 4: printf("four\npatru");
            break;
        case 5: printf("five\ncinci");
            break;
        case 6: printf("six\nsase");
            break;
        case 7: printf("seven\nsapte");
            break;
        case 8: printf("eight\nopt");
            break;
        case 9: printf("nine\nnoua");
            break;
        default: printf("invalid");
            break;
    }
}
                     
                       




